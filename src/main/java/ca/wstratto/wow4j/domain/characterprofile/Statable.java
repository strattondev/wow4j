package ca.wstratto.wow4j.domain.characterprofile;

public class Statable {
    private int health;
    private int power;

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getPower() {
        return power;
    }

    public void setPower(int power) {
        this.power = power;
    }
}
